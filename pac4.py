# Function to draw a square tile
def square(x, y):
    """Draw square using path at (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

# Function to get the offset of a point in tiles
def offset(point):
    """Return offset of point in tiles."""
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

# Function to check if a point is valid in tiles
def valid(point):
    """Return True if point is valid in tiles."""
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

# Function to draw the world (background and tiles)
def world():
    """Draw world using path."""
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

# Main move function for Pac-Man and ghosts
def move():
    """Move pacman and all ghosts."""
    # Update the score on the screen
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    # Draw Pac-Man
    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    # Move the ghosts
    for point, course in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            options = [
                vector(5, 0),
                vector(-5, 0),
                vector(0, 5),
                vector(0, -5),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10)
        dot(20, 'red')

    update()

    # Check for collision with ghosts (Game Over)
    for point, course in ghosts:
        if abs(pacman - point) < 20:
            game_over()

    # Keep the game running
    if not state['game_over']:
        ontimer(move, 100)

# Function to change Pac-Man's direction
def change(x, y):
    """Change pacman aim if valid."""
    if valid(pacman + vector(x, y)):
        aim.x = x
        aim.y = y

# Function to display Game Over screen
def game_over():
    """Display the game over screen."""
    state['game_over'] = True
    writer.goto(0, 0)
    writer.color('red')
    writer.write(f"Game Over! Final Score: {state['score']}", align="center", font=("Arial", 16, "normal"))

